#include <bits/stdc++.h>
using namespace std;

int main()
{
    int n;
    cin >> n;
    while (n--)
    {
        int q;
        cin >> q;
        vector<int> v(q);
        for (int i = 0; i < q; i++)
        {
            int a;
            cin >> a;
            v.push_back(a);
        }
    }
    return 0;
}
